echo "üîß –ù–∞—Å—Ç—Ä–∞–∏–≤–∞–µ–º –æ–∫—Ä—É–∂–µ–Ω–∏–µ –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ..."
ssh $SERVER_USER@$SERVER_HOST << EOF
    # –°–æ–∑–¥–∞–µ–º –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é –¥–ª—è –ø—Ä–æ–µ–∫—Ç–∞, –µ—Å–ª–∏ –µ—ë –Ω–µ—Ç
    mkdir -p $DEPLOY_DIR
    cd $DEPLOY_DIR
    
    # –°–æ–∑–¥–∞–µ–º –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã–µ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏
    mkdir -p data logs backup
    chmod 755 data logs backup
    
    # –†–∞—Å–ø–∞–∫–æ–≤—ã–≤–∞–µ–º –∞—Ä—Ö–∏–≤
    tar -xzf /tmp/deploy.tar.gz -C $DEPLOY_DIR
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ Docker –∏ Docker Compose
    if ! command -v docker &> /dev/null || ! command -v docker-compose &> /dev/null; then

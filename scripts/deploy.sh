        echo "–£—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ –∏—Ö –∫–æ–º–∞–Ω–¥–æ–π:"
        echo "curl -fsSL https://get.docker.com | sh && apt-get install -y docker-compose"

    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ .env —Ñ–∞–π–ª–∞, –µ—Å–ª–∏ –Ω–µ—Ç - –∫–æ–ø–∏—Ä—É–µ–º –ø—Ä–∏–º–µ—Ä
    if [ ! -f .env ]; then

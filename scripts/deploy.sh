        backup_file="backup/finance_\$(date +%Y%m%d_%H%M%S).db"
        cp data/finance.db "\$backup_file"
        echo "üìë –°–æ–∑–¥–∞–Ω–∞ —Ä–µ–∑–µ—Ä–≤–Ω–∞—è –∫–æ–ø–∏—è –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö: \$backup_file"
    fi
    
    # –ó–∞–ø—É—Å–∫–∞–µ–º –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä—ã
